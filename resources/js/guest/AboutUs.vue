    methods: {
        submitContact(e) {
            this.loading = true;

            setTimeout(() => {
                this.loading = false;
                axios
                    .post('/api/contacts', {
                        name: this.form.name,
                        email: this.form.email,
                        message: this.form.message
                    })
                    .then(() => {
                        this.$swal({
                            position: 'center',
                            icon: 'success',
                            title: 'Message Sent Successfully.',
                            showConfirmButton: false,
                            timer: 1500
                        }).then(response => {
                            this.$router.push({
                                name: 'home'
                            });
                            console.log(response.data);
                        });
                    })
                    .catch(error => {
                        this.errors = error.response.data.errors;
                    });
            }, 2000);

            e.preventDefault();
        }
    }
};
